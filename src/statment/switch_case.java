package statment;

public class switch_case {
	public static void main( String[] args) {
		
		int choice = 2;
		
		switch (choice) {
		
		case 1: 
			
			System.out.println("gujrati");
			break;
			
		case 2 : 
			
			System.out.println("hindi");
			break;
			
		case 3 :
		   System.out.println("english");
		   break;
		   
		default :
			System.out.println("invalid choice");
		   
		}
		
	}
}

